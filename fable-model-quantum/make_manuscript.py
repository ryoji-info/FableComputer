def para(text="", style=None, italic=False, bold=False, center=False,
         size=None, space_before=None):
    p = doc.add_paragraph(style=style)
    if center:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if space_before is not None:
        p.paragraph_format.space_before = Pt(space_before)
    if text:
        r = p.add_run(text)
        r.italic = italic
        r.bold = bold
        if size:
            r.font.size = Pt(size)
    return p


def rich(pieces, center=False):
    """pieces: list of (text, dict of run props)."""
    p = doc.add_paragraph()
    if center:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for text, props in pieces:
        r = p.add_run(text)
        for k, v in props.items():
            setattr(r, k, v) if k in ("bold", "italic") else None
            if k == "size":
                r.font.size = Pt(v)
    return p


def heading(text, level=1):
    doc.add_heading(text, level=level)


def equation(text, tag):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = p.add_run(text + "        ")
    r.italic = True
    p.add_run(f"({tag})")


def table(headers, rows, caption=None, widths=None, font_size=9):
    t = doc.add_table(rows=1 + len(rows), cols=len(headers))
    t.style = "Light Grid Accent 1"
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for j, h in enumerate(headers):
        cell = t.rows[0].cells[j]
        cell.text = ""
        r = cell.paragraphs[0].add_run(h)
        r.bold = True
        r.font.size = Pt(font_size)
    for i, row in enumerate(rows):
        for j, val in enumerate(row):
            cell = t.rows[i + 1].cells[j]
            cell.text = ""
            r = cell.paragraphs[0].add_run(str(val))
            r.font.size = Pt(font_size)
    if widths:
        for j, w in enumerate(widths):
            for row in t.rows:
                row.cells[j].width = Inches(w)
    if caption:
        para(caption, italic=True, size=9)
    return t


def figure(path, caption, width=6.2):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.add_run().add_picture(os.path.join(HERE, "figures", path),
                            width=Inches(width))
    para(caption, italic=True, size=9)


