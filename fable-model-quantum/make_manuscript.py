_SUP = str.maketrans("0123456789-", "⁰¹²³⁴⁵⁶⁷⁸⁹⁻")


def sci(x, digits=1):
    """1.3e-06 -> '1.3×10⁻⁶' (unicode superscripts, parent style)."""
    if x == 0:
        return "0"
    exp = math.floor(math.log10(abs(x)))
    mant = x / 10**exp
    m = f"{mant:.{digits}f}"
    if float(m) >= 10:          # rounding pushed mantissa to 10
        m, exp = f"{float(m)/10:.{digits}f}", exp + 1
    return f"{m}×10{str(exp).translate(_SUP)}"


