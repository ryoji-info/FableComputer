# ===================== 7. what is and is not quantum =====================
heading("7. What is, and is not, quantum here")
para("Is: the noise. Below T_Q the decision noise becomes vacuum-dominated "
     "— the machine computes against ħω/2 per mode rather "
     "than a thermal bath — and from ≈15–20 K down its error "
     "floors are set by quantum mechanics (vacuum variance, the Caves cost "
     "of the decoder's gain), not by temperature. The per-shot information of one pulse is "
     "shot-noise-bounded (Figure Q4A). These are quantitative, falsifiable "
     "quantum statements, and gate QG2 tests the sharpest of them.")
para("Is not: the computation. Coherent states, beamsplitters, "
     "phase-insensitive amplifiers and homodyne decisions are all Gaussian; "
     "a Gaussian machine with coherent-state inputs is efficiently "
     "classically simulable, and universal continuous-variable quantum "
     "computation requires exactly the non-Gaussian resource [7, 8] that "
     "Section 3.3 shows this cell cannot supply (χ/κ ≈ "
     "10⁻⁵). No entanglement is generated or claimed anywhere in "
     "QMAC-1. The honest name for this machine is quantum-limited analog "
     "computing: classical arithmetic executed at the precision ceiling "
     "quantum mechanics assigns to unentangled light.")
para("The upgrade paths are named, with their prices. (i) Blockade cells: "
     "≈3-nm structures at χ/κ ≈ 1 [6] — a different "
     "lithography universe, and lifetime-limited even then. (ii) Squeezed "
     "operation: a DS cell driven parametrically (bias modulated at "
     "2f₀) would be a phase-sensitive amplifier, which evades the "
     "F = 2 − 1/G floor and could push decisions below the standard "
     "quantum limit; graphene plasmon squeezing has no experimental "
     "precedent and is listed as an open item, not an ingredient. (iii) "
     "Entanglement distribution across the fabric: plasmonic channels "
     "preserve entanglement at optical frequencies [4]; nothing comparable "
     "exists at THz. None of these is assumed by any number in this Part.")

